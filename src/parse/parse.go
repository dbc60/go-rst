func (t *Tree) blockquote(i *item) Node {
	s := i
	if i.Type != itemSpace {
		// If i is not itemSpace, it is a itemBlockQuote. In that case we will get the last itemSpace token found to
		// use for the indent level calculation.
		s = t.peekBackTo(itemSpace)
	}

	level := s.Length / t.indentWidth
	t.log.WithFields(log.Fields{"indentLevel": t.indentLevel, "level": level}).Debugf("blockquote: indent level")
	if t.indentLevel == level {
		i.Type = itemText
		return newParagraph(i, &t.id)
	}

	if i.Type == itemSpace {
		if t.peek(1).Type != itemBlockQuote {
			t.indentLevel = level
			return newBlockQuote(
				&item{Type: itemBlockQuote, Line: i.Line},
				level, &t.id)
		}
		t.log.Debug("Next item is itemBlockQuote")
		return nil
	}

	levelChanged := false
	if t.indentLevel != level {
		t.log.WithField("indentLevel", level).Debug("blockquote: Setting indentLevel")
		t.indentLevel = level
		levelChanged = true
	}

	var sec Node
	n := *i

	if levelChanged {
		// FIXME: Code a token ring insertion API
		t.token[zed+1] = &n
		t.indentLevel = level
		sec = newBlockQuote(&item{Type: itemBlockQuote, Line: i.Line, StartPosition: i.StartPosition,
			Length: i.Length}, level, &t.id)
	}

	return sec
}

func (t *Tree) definitionTerm(i *item) Node {
	sec := t.definitionList
	if sec == nil {
		sec = newDefinitionList(&item{Line: i.Line}, &t.id)
		t.definitionList = sec
		t.nodeTarget.append(sec)
		t.next(1)
	}
	t.nodeTarget = &sec.NodeList

	// Container for definition items
	dli := newDefinitionListItem(i, t.peek(1), &t.id)
	t.nodeTarget.append(dli)
	t.nodeTarget = &dli.Definition.NodeList

	// Gather definitions and body elements
	for {
		ni := t.next(1)
		if ni == nil {
			break
		}
		if ni.Type == itemSpace {
			continue
		} else if ni.Type == itemDefinitionText {
			np := newParagraph(ni, &t.id)
			dli.Definition.NodeList.append(np)
			t.nodeTarget = &np.NodeList
			t.next(1)
			continue
		} else if ni.Type == itemDefinitionTerm {
			t.nodeTarget = &sec.NodeList
			t.definitionTerm(ni)
			continue
		}
		if ni.Type == itemEOF {
			break
		}
		t.nodeTarget = &dli.Definition.NodeList
		t.subParseBodyElements(ni)
	}

	return sec
}

